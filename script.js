const categorySelect = document.getElementById("category");
const wazaList = document.getElementById("wazaList");
const randomWaza = document.getElementById("randomWaza");
const button = document.getElementById("selectBtn");

function loadWaza(cat) {
  const list = techMap[cat] || [];
  wazaList.textContent = list.join("\n");
  randomWaza.textContent = "-----";
}

function selectRandom(cat) {
  const list = techMap[cat] || [];
  if (list.length === 0) return;
  const index = Math.floor(Math.random() * list.length);
  randomWaza.textContent = list[index];
}

categorySelect.addEventListener("change", () => {
  loadWaza(categorySelect.value);
});

button.addEventListener("click", () => {
  selectRandom(categorySelect.value);
});

